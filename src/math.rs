/// A two-dimensional non-generic point (struct) with an `x` (x-coordinate) and `y` (y-coordinate)
pub struct Point2D {
    pub x: f64,
    pub y: f64,
}

/// A three-dimensional non-generic point (struct) with an `x` (x-coordinate), `y` (y-coordinate) and `z` (z-coordinate)
pub struct Point3D {
    pub x: f64,
    pub y: f64,
    pub z: f64,
}
